package com.questions;

public class LengthFindings {
	public static void main(String[] args) {
		
		String s1 = "Automation Testing";
		String s2 = "Information";
		String s3 = "Tamilnadu";
		String s4 = "j a v a p r o g r a m";
		String s5 = "987654321";
		
		int length = s1.length();
		int length2 = s2.length();
		int length3 = s3.length();
		int length4 = s4.length();
		int length5 = s5.length();
		
		System.out.println("Automation Testing : " + length);
		System.out.println("Information : " + length2);
		System.out.println("Tamilnadu : " + length3);
		System.out.println("j a v a p r o g r a m : " + length4);
		System.out.println("987654321 : " + length5);
		
	}
}
